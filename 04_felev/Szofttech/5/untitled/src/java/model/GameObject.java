package model;

public abstract class GameObject {
    int x = 0;
    int y = 0;
    int width = 0;
    int height = 0;

    public int getX() {
        return x;
    }
    public int getY() {
        return y;
    }
    public int getWidth() {
        return width;
    }
    public int getHeight() {
        return height;
    }
}
